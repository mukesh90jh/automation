package concepts.src.pattern;

public class Pattern2 {

	
	public static void main(String[] args) 
	{
		int i,j;
        for(i=1;i<=7;i+=2)
        {
        	for(j=1;j<=i;j+=2)
        	{
        		System.out.print(j+"\t");
        		
        	}
        	System.out.print("\n");
        }	

	}

}
